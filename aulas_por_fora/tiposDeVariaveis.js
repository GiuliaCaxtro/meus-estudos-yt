//variaveis constantes e tipos em JavaScript
Number   //sao os tipos de variáveis em JavaScript que contem numeros, tanto inteiros quanto decimais ex; 42, 3.14
String   //sao os tipos de variáveis em JavaScript que contem texto, ex; "Olá, mundo!"
Boolean  //sao os tipos de variáveis em JavaScript que contem valores lógicos ex; true ou false
null     //sao os tipos de variáveis em JavaScript que contem o valor nulo ex; null
undefined //sao os tipos de variáveis em JavaScript que contem o valor indefinido ex; undefined

