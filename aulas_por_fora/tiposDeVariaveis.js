//regras de identificadores em JavaScript
//1. Os identificadores devem começar com uma letra, um cifrão ($) ou um sublinhado (_).
//2. Os identificadores podem conter letras, dígitos, cifrões ($) e sublinhados (_), mas não podem conter espaços ou caracteres especiais.
//3. Os identificadores são sensíveis a maiúsculas e minúsculas, ou seja, "variavel" e "Variavel" são considerados identificadores diferentes.
//4. Os identificadores não podem ser palavras reservadas do JavaScript, como "var", "let", "const", "if", "else", etc.
//5. caracteres unicode são permitidos em identificadores, o que significa que você pode usar caracteres de outros idiomas, como acentos ou caracteres chineses, desde que sigam as regras acima.ex; "variável", "变量", "variável_com_acento".

